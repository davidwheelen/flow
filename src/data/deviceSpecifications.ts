export function getDevicesByFamily(family: string): DeviceSpecification[] {
  return deviceSpecifications.filter((spec) => 
    spec.family.toLowerCase() === family.toLowerCase()
  );
}

/**
 * Get device icon path by model name
 */
export function getDeviceIcon(model: string): string {
  const spec = getDeviceSpecification(model);
  return spec ? `/iconpacks/isoflow-default/${spec.icon}.svg` : '/iconpacks/isoflow-default/router.svg';
}
